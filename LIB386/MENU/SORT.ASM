			.386P
			.model FLAT, SYSCALL

;----------------------------------------------------------------------------
			.data

;----------------------------------------------------------------------------

			public	SYSCALL		MySortCompFunc

;----------------------------------------------------------------------------
ifdef	REG_CALL
			extrn	SYSCALL		strcmp_	:PROC
else
			extrn	SYSCALL		strcmp	:PROC
endif

;----------------------------------------------------------------------------
			.code

;//-------------------------------------------------------------------------
ifdef	REG_CALL

;//-------------------------------------------------------------------------
MySortCompFunc 		proc

			mov	eax, [eax]
			mov	edx, [edx]

			jmp	strcmp_

MySortCompFunc		endp

;//-------------------------------------------------------------------------
else

;//-------------------------------------------------------------------------
MySortCompFunc 		proc	,\
			ptra:DWORD, ptrb:DWORD

			mov	eax, [ptrb]
			mov	edx, [ptra]

			mov	eax, [eax]
			mov	edx, [edx]

			push	eax
			push	edx

			call	strcmp

			add	esp, 8

			ret

MySortCompFunc		endp

;//-------------------------------------------------------------------------
endif

;----------------------------------------------------------------------------
;				The
				End
