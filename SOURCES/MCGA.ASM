				.386P
				.model SMALL, SYSCALL
	JUMPS
;----------------------------------------------------------------------------
				.data
;----------------------------------------------------------------------------
			include	\projet\lib386\lib_svga\svga.ash
;----------------------------------------------------------------------------
			public	NoLanguage	Mcga_Cls
			public	NoLanguage	Mcga_Flip

			.code

;//--------------------------------------------------------------------------
Mcga_Cls		proc	uses	edi

			xor	eax, eax

			mov	edi, Log
			mov	ecx, 64000/4
			rep	stosd

			ret
Mcga_Cls		endp

;//--------------------------------------------------------------------------
Mcga_Flip		proc	uses	esi edi

			mov	esi, Log
			mov	edi, Phys

			mov	ecx, 64000/4
			rep	movsd

			ret
Mcga_Flip		endp

